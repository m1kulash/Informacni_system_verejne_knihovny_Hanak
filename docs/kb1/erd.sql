  ISBN NVARCHAR(20) NULL UNIQUE,
  RokVydani SMALLINT NULL,
  Nosic NVARCHAR(30) NOT NULL,
  StrucnyObsah NVARCHAR(MAX) NULL,
  VydavatelID INT NOT NULL REFERENCES Vydavatel(VydavatelID)
);

