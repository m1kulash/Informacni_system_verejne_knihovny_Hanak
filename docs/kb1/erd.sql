CREATE TABLE Autor (
  AutorID INT IDENTITY PRIMARY KEY,
  Jmeno NVARCHAR(100) NOT NULL,
  Prijmeni NVARCHAR(100) NOT NULL
);

CREATE TABLE Zanr (
  ZanrID INT IDENTITY PRIMARY KEY,
  Nazev NVARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE Vydavatel (
  VydavatelID INT IDENTITY PRIMARY KEY,
  Nazev NVARCHAR(150) NOT NULL UNIQUE
);

CREATE TABLE Titul (
  TitulID INT IDENTITY PRIMARY KEY,
